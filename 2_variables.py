# variables are containers for storing data values
# assigning some values is all that's needed for variable creation
# no need to declare with the type
# determines the type depending upon the assigned value
# integer
# type function is used for finding the type of variable
a = 22
print(a)
print(type(a))

# float
b = 22.5
print(b)
print(type(b))

# string
c = "this is test string"
print(c)
print(type(c))

# boolean
d = True
print(d)
print(type(d))

