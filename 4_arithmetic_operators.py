# check entering any two numbers
# input function being used along with prompting the user
# typecasting to integer type
# addition
num1 = int(input("Enter first number "))
num2 = int(input("Enter second number "))
result = num1 + num2
print(f"Sum of {num1} and {num2} is {result}")

# subtraction
result = num1 - num2
print(f"Difference of {num1} and {num2} is {result}")

# multiplication
result = num1 * num2
print(f"Product of {num1} and {num2} is {result}")

# division
result = num1 / num2
print(f"Division of {num1} and {num2} is {result}")

# floor division
result = num1 // num2
print(f"Floor division of {num1} and {num2} is {result}")

# remainder
result = num1 % num2
print(f"Remainder of {num1} and {num2} is {result}")

